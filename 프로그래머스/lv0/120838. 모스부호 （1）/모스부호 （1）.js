function solution(letter) {
    const morse = [".-","-...","-.-.","-..",".","..-.","--.","....","..",".---","-.-",".-..","--","-.","---",".--.","--.-",".-.","...","-","..-","...-",".--","-..-","-.--","--.."];
    
    return String.fromCharCode(...letter.split(' ').map(v => morse.indexOf(v) + 97));
}